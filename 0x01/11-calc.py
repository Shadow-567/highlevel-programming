#!/usr/bin/python3
"""a module that implements a basic calculator"""


def add(a, b):
    """
       A function that adds two numbers
       Args:
           a: any positive or negtive numbers
           b: any positive or negative numbers
       Return:
           the sum of a and b
    """
    return a + b


def sub(a, b):
    """
       A function that subtracts two numbers
       Args:
           a: any positive or negtive numbers
           b: any positive or negative numbers
       Return:
           the subtraction of a and b
    """
    return a - b


def mul(a, b):
    """
       A function that multiplies two numbers
       Args:
           a: any positive or negtive numbers
           b: any positive or negative numbers
       Return:
           the multiplication of a and b
    """
    return a * b


def div(a, b):
    """
       A function that divides two numbers
       Args:
           a: any positive or negtive numbers
           b: any positive or negative numbers
       Return:
           the division of a and b
    """
    return a / b


def exp(a, b):
    """
       A function that exponciates two numbers
       Args:
           a: any positive or negtive numbers
           b: any positive or negative numbers
       Return:
           the exponiatial of a and b
    """
    return a ** b


def mod(a, b):
    """
       A function that gives the modulus of two numbers
       Args:
           a: any positive or negtive numbers
           b: any positive or negative numbers
       Return:
           the modulus of a and b
    """
    return a % b